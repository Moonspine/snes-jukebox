// Color format is either normal:
//   0b RRRR RGGG GGGB BBBB
// or flipped:
//   0b BBBB BGGG GGGR RRRR
#ifdef FLIP_COLOR_R_B
  #define COLOR(x) ((x & 0xF800) >> 11) + (x & 0x07E0) + ((x & 0x001F) << 11)
#else
  #define COLOR(x) x
#endif

